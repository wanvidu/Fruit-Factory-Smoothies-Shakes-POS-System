    {//GEN-HEADEREND:event_jTextFieldNewUnitPriceKeyTyped
        char key = evt.getKeyChar();

        if (!(Character.isDigit(key)) || key == KeyEvent.VK_BACKSPACE || key == KeyEvent.VK_DELETE)
        {
            evt.consume();
        }

        if (jTextFieldNewUnitPrice.getText().length() >= 4)
        {
            evt.consume();
        }
    }//GEN-LAST:event_jTextFieldNewUnitPriceKeyTyped

