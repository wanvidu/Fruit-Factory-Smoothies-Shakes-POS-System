    private void jButtonDeleteIngredientsActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonDeleteIngredientsActionPerformed
    {//GEN-HEADEREND:event_jButtonDeleteIngredientsActionPerformed

        //Delete an item from Ingredients123456789 table.
        DefaultTableModel model = (DefaultTableModel) jTableManageIngredients.getModel();

        if (jTableManageIngredients.getSelectedRow() == -1)
        {
            if (jTableManageIngredients.getRowCount() == 0)
            {
                JOptionPane.showMessageDialog(rootPane, "No Data to Delete.", "Manage Ingredients", JOptionPane.OK_OPTION);
            } else
            {
                JOptionPane.showMessageDialog(rootPane, "Select a Row to Delete.", "Manage Ingredients", JOptionPane.OK_OPTION);
            }
        } else
        {
            String ID = jTableManageIngredients.getValueAt(jTableManageIngredients.getSelectedRow(), 0).toString();

            ID = "DELETE FROM Ingredients123456789 WHERE IngredientID=" + ID;

            boolean status = conn.deleteRow(ID);

            if (status)
            {
                JOptionPane.showMessageDialog(rootPane, "Successfully Deleted.", "Manage Ingredients", JOptionPane.PLAIN_MESSAGE);
                model.removeRow(jTableManageIngredients.getSelectedRow());
            }
        }
    }//GEN-LAST:event_jButtonDeleteIngredientsActionPerformed

