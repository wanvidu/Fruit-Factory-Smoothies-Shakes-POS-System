    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonAddActionPerformed
    {//GEN-HEADEREND:event_jButtonAddActionPerformed
        //Adding new Ingredients to the Ingredients123456789 table.

        int ID = Integer.parseInt(jTextFieldIIngredientID.getText());
        String name = jTextFieldName.getText();
        int unitPrice = Integer.parseInt(jTextFieldUnitPrice.getText());

        String query = "INSERT INTO Ingredients123456789 VALUES (" + ID + ",'" + name + "'," + unitPrice + ")";

        System.out.println(query);

        boolean status = conn.insert(query);

        jDialogAddIngredient.setVisible(false);

        //Refresh jTableManageIngredients table.
        emptyTable();

        displayTable();

        if (status)
        {
            JOptionPane.showMessageDialog(rootPane, "Successfully Added.", "Add Ingredient", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

