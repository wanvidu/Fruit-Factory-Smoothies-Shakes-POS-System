    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonAddActionPerformed
    {//GEN-HEADEREND:event_jButtonAddActionPerformed
        ////Adding new Employee to the Emp123456789 table.
        
        int ID = Integer.parseInt(jTextFieldIID.getText());
        String NIC = jTextFieldNIC.getText();
        String name = jTextFieldName.getText();
        int age = Integer.parseInt(jTextFieldAge.getText());
        String password = jPasswordFieldPassword.getText();

        NIC = NIC + "v";

        String query = "INSERT INTO Emp123456789 VALUES (" + ID + ",'" + NIC + "','" + name + "'," + age + ",'" + password + "')";

        boolean status = conn.insert(query);

        jDialogAddEmployee.setVisible(false);

        refreshTable();

        displayTable();

        if (status)
        {
            JOptionPane.showMessageDialog(rootPane, "Successfully Added.", "Add Employee", JOptionPane.PLAIN_MESSAGE);
        }

    }//GEN-LAST:event_jButtonAddActionPerformed

