    //Method to populate jTableManageEmployee table.

    void displayTable()
    {
        try
        {
            conn.get("SELECT * FROM Emp123456789");

            while (conn.rs.next())
            {
                String ID = conn.rs.getString(1);
                String NIC = conn.rs.getString(2);
                String Name = conn.rs.getString(3);
                String Age = conn.rs.getString(4);

                Object[] content =
                {
                    ID, NIC, Name, Age
                };

                DefaultTableModel model = (DefaultTableModel) jTableManageEmployee.getModel();

                model.addRow(content);
            }
        } catch (Exception e)
        {
        }

    }
    
     //Method to emptying out jTableManageEmployee table.

    void refreshTable()
    {
        DefaultTableModel model = (DefaultTableModel) jTableManageEmployee.getModel();

        int count = model.getRowCount();

        for (int i = 0; i < count; i++)
        {
            model.removeRow(0);
        }

    }

