    private void jButtonDeleteEmployeeActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonDeleteEmployeeActionPerformed
    {//GEN-HEADEREND:event_jButtonDeleteEmployeeActionPerformed

        //Delete an item from Ingredients123456789 table.
        
        DefaultTableModel model = (DefaultTableModel) jTableManageEmployee.getModel();

        if (jTableManageEmployee.getSelectedRow() == -1)
        {
            if (jTableManageEmployee.getRowCount() == 0)
            {
                JOptionPane.showMessageDialog(rootPane, "No Data to Delete.", "Manage Employee", JOptionPane.OK_OPTION);
            } else
            {
                JOptionPane.showMessageDialog(rootPane, "Select a Row to Delete.", "Manage Employee", JOptionPane.OK_OPTION);
            }
        } else
        {
            String ID = jTableManageEmployee.getValueAt(jTableManageEmployee.getSelectedRow(), 0).toString();

            ID = "DELETE FROM Emp123456789 WHERE EmpID=" + ID;

            boolean status = conn.deleteRow(ID);

            if (status)
            {
                JOptionPane.showMessageDialog(rootPane, "Successfully Deleted.", "Edit Employee", JOptionPane.PLAIN_MESSAGE);
                model.removeRow(jTableManageEmployee.getSelectedRow());
            }
        }
    }//GEN-LAST:event_jButtonDeleteEmployeeActionPerformed

